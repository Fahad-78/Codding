#Integer
print(8)

print(1e308) #1*10^308
print(1e309) #Inf

#Decimal/Float
print(8.55)
print(1.7e308) #1.7*10^308

#Boolean
print(True) #True -> 1
print(False) #False -> 0

#Text/String
print('Hello World!')

#Complex
print(5+6j)

#List-> c-> Array
print([1, 2, 3, 4, 5])

#Tuple
print((1, 2, 3, 4, 5))

#sets
print({1, 2, 3, 4, 5})

#Dictionary
print({'name':'Fahad', 'gender':'Male','weight':52})

#Type function
print(type(8))
print(type(1e308))
print(type(1.7e308))
print(type(True))
print(type('Hello World!'))
print(type(5+6j))
print(type([1, 2, 3, 4, 5]))
print(type((1, 2, 3, 4, 5)))
print(type({1, 2, 3, 4, 5}))
print(type({'name':'Fahad', 'gender':'Male','weight':52}))

