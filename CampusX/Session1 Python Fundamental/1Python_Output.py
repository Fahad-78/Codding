#Python is a case sensitive language
print('Hello World!')

print('Salman Khan')

print(7)

print(7.7)

print(True)

print('Hello', 1, 4.5, True)

#Changing the seperate behaviour 
print('Hello', 1, 4.5, True, sep=',')

print('Hello')
print('World')

#Changing the endline behaviour
print('Hello',end=' ')
print('World')